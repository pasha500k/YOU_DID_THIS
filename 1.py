import os

print("Программа добавления скрытого текста в изображение")

# запрос пути к картинке
image_path = input("Введите путь к изображению: ").strip()

if not os.path.exists(image_path):
    print("Файл не найден")
    exit()

# запрос текста
text = input("Введите текст который нужно спрятать: ")

# имя нового файла
output = "new_" + os.path.basename(image_path)

# чтение картинки
with open(image_path, "rb") as img:
    data = img.read()

# запись новой картинки
with open(output, "wb") as new_img:
    new_img.write(data)
    new_img.write(b"\n")
    new_img.write(text.encode("utf-8"))
    new_img.write(b"\n")

print("Готово")
print("Новый файл:", output)
print("Картинка будет открываться нормально, а текст будет виден в блокноте.")
